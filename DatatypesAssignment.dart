void main() {
  // int data type
  int age = 27;
  print('Age: $age');

  // double data type
  double height = 5.8;
  print('Height: $height feet');

  // String data type
  String name = 'Chebet Ropkoy';
  print('Name: $name');

  // List data type
  List<String> fruits = ['Grapes', 'Lemon', 'Orange'];
  print('Fruits: $fruits');

  // Map data type
  Map<String, int> scores = {
    'Math': 84,
    'Chemistry': 73,
    'English': 89
  }; // declaring a map
  print('Scores: $scores');
}
